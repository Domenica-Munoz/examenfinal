package ups.edu.ec.app.gp2.CajeraJEE.MunozDomenicaCajeraJEE.util;

public class Cajero {
	
 private String cedula;
 private String celular;
 private boolean valor_recarga;
 
 
 
public String getCedula() {
	return cedula;
}
public void setCedula(String cedula) {
	this.cedula = cedula;
}
public String getCelular() {
	return celular;
}
public void setCelular(String celular) {
	this.celular = celular;
}
public boolean isValor_recarga() {
	return valor_recarga;
}
public void setValor_recarga(boolean valor_recarga) {
	this.valor_recarga = valor_recarga;
}
 
 
 

}
